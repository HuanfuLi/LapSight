internal val LocalStrings = staticCompositionLocalOf { StringsEn }

internal val strings: LocalizedStrings
    @Composable
    @ReadOnlyComposable
    get() = LocalStrings.current

@Composable
internal fun ProvideLocalizedStrings(
    languageMode: LanguageMode,
    content: @Composable () -> Unit,
) {
    CompositionLocalProvider(
        LocalStrings provides stringsFor(resolveAppLanguage(languageMode, systemLanguageTag())),
        content = content,
    )
}

private fun stringsFor(language: AppLanguage): LocalizedStrings = when (language) {
    AppLanguage.English -> StringsEn
    AppLanguage.Chinese -> StringsZh
    AppLanguage.Korean -> StringsKo
    AppLanguage.Japanese -> StringsJa
    AppLanguage.French -> StringsFr
    AppLanguage.Spanish -> StringsEs
}

