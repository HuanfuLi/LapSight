internal enum class AppLanguage {
    English,
    Chinese,
    Korean,
    Japanese,
    French,
    Spanish,
}

internal fun resolveAppLanguage(
    mode: LanguageMode,
    systemTag: String,
): AppLanguage = when (mode) {
    LanguageMode.System -> matchSystemLanguage(systemTag)
    LanguageMode.English -> AppLanguage.English
    LanguageMode.Chinese -> AppLanguage.Chinese
    LanguageMode.Korean -> AppLanguage.Korean
    LanguageMode.Japanese -> AppLanguage.Japanese
    LanguageMode.French -> AppLanguage.French
    LanguageMode.Spanish -> AppLanguage.Spanish
}

private fun matchSystemLanguage(systemTag: String): AppLanguage {
    val language = systemTag.trim().lowercase()
        .substringBefore('-')
        .substringBefore('_')
    return when (language) {
        "zh" -> AppLanguage.Chinese
        "ko" -> AppLanguage.Korean
        "ja" -> AppLanguage.Japanese
        "fr" -> AppLanguage.French
        "es" -> AppLanguage.Spanish
        else -> AppLanguage.English
    }
}

